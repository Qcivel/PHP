        <p>BIOGRAPHIE : THÉO RENAUT <br><br>Théo Renaut est né en France en 1992.Il vit et travaille entre Toulouse et Ouagadougou.Il a commencé à travailler dans l’univers de l’image à partir de 16 ans en tant qu’assistant cameraman où il découvre le Burkina Faso grâce au réalisateur Claude Pfaffmann. En 2013, il décide de s’y installer et y restera environ trois ans.Travaillant sur l’actualité pour la presse, il développe en parallèle plusieurs projets au long cours comme African Far West, Ouagadougou, Zoanga, … Ses photos sont publiées dans des magazines tels que le New York Times, le Guardian, le Sunday Times, Le Monde, Libération, …Son travail African Far West sur les mines d’or clandestines du Burkina Faso a été exposé à Aix-en-Provence, Ouagadougou, …
            Depuis janvier 2017, il collabore avec Bake un artiste Franco-Burkinabé, sur une série intitulée « Schizophrenic therapy »
